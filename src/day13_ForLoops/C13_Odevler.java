package day13_ForLoops;

public class C13_Odevler {
    public static void main(String[] args) {
// 0 dan 100 e kadar olan sayıları ekrana yazdırın.
        for (int i=0; i<=100; i++){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("-------------------------");
        //15 den 100 e kadar olan sayıları ekrana yazdırın.
        for (int i=15; i<=100; i++){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("---------------------------");
        //0 dan 100 e kadar olan sayıları tersten ekrana yazdırın.
        for (int i=100; 0<=i; i--){
            System.out.print(i--+" ");
        }
        System.out.println();
        System.out.println("---------------------------");
        //0 da 100 e kadar olan çift sayıları ekrana yazdırın.


    }
}
