package day15_If_Else_Switch_Loops_Summary;

public class C09_Nested_Loops {
    public static void main(String[] args) {
        for (int i=0; i<3 ; i++){
            System.out.println("A");
            for (int j=0; j <2; j++){
                System.out.println("B");
            }
            System.out.println();
        }

    }


}
