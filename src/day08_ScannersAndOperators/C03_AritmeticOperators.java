package day08_ScannersAndOperators;

public class C03_AritmeticOperators {
    public static void main(String[] args) {
        System.out.println("90"+10);  // 9010,concatanation
        System.out.println("90"+10);  //    ,concatanation

        System.out.println(10.0/4);

        System.out.println("-----------------");
        int a = 100;
        double b = a/6;
        double c = 16;
        System.out.println(b);
        System.out.println(c);

        double d = (double) a/b;
        System.out.println(d);

        System.out.println("-----------------");

    }
}
